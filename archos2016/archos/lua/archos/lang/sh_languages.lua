if CLIENT then

	function ARCHOS.OpenLanguageMenu()
		local Frame = vgui.Create("DFrame")
		Frame:SetSize(250,165)
		Frame:SetVisible(true)
		Frame:Center()
		Frame:MakePopup()
		Frame:ShowCloseButton(false)
		Frame:SetTitle("")
		Frame.Paint = function(s,w,h)
			draw.RoundedBox(2,0,0,w,h,ARCHOS.Colors["background"])
			draw.RoundedBox(2,0,0,w,25,ARCHOS.Colors["header"])
		end
		
		for k,v in pairs(ARCHOS.Languages.List.Name) do
			local LangButton = vgui.Create("DButton", Frame)
			LangButton:SetText(v.." (By ".. ARCHOS.Languages.List.Author[k] ..")")
			LangButton:SetTextColor(ARCHOS.Colors["text"])
			LangButton:SetFont("ARCHOS_Text")
			LangButton:SetPos(15,k*31)
			LangButton:SetSize(220, 25)
			LangButton.Paint = function(self, w, h)
				surface.SetDrawColor(ARCHOS.Colors["header"])
				surface.DrawRect(0, 0, w, h)
				
				surface.SetDrawColor(Color(255,255,255,255))
				surface.SetMaterial(ARCHOS.Languages.List.Icon[k])
				surface.DrawTexturedRect( 4, 7, 16, 10 )
			end
			LangButton.DoClick = function()
				cookie.Set("archos_language",ARCHOS.Languages.List.ID[k])
				ARCHOS.Languages.Functions[cookie.GetString("archos_language")]()
				chat.AddText(Color(0,255,255),"[ArchOS] ",Color(255,255,255),ARCHOS.CurrentLanguage["setlang"])
				Frame:Remove()
			end
		end	
	end
	
	if IsValid(LocalPlayer()) then
		if(cookie.GetString("archos_language",false) == false) then
			ARCHOS.Languages.Functions[1]()
		else
			ARCHOS.Languages.Functions[cookie.GetString("archos_language")]()
		end
	end
	
	hook.Add("InitPostEntity","ARCHOS_Language_Load",function()
		net.Start( "ARCHOS_Language_Joined" )
		net.SendToServer()
	end)
	
	concommand.Add("archos_languagemenu",function(ply,cmd,args)
		ARCHOS.OpenLanguageMenu()
	end)
	
	net.Receive("ARCHOS_Language_OpenMenu",function()
		ARCHOS.OpenLanguageMenu()
	end)
	
	net.Receive("ARCHOS_Language_Load",function()
		ARCHOS.Languages.Functions[cookie.GetString("archos_language")]()
	end)

end